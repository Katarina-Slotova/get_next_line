#include "libft.h"

void	ft_putendl(char const *s)
{
	int	a;

	a = 0;
	if (!s)
		return ;
	while (s[a] != '\0')
	{
		ft_putchar(s[a]);
		a++;
	}
	ft_putchar('\n');
}
